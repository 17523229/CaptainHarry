#Exercise

#Constant Functions

f1 <- function(x){
  result <- 5
  return(result)
}
f1(2)

#Linear Functions

f2 <- function(x){
  result <- 2*x+2
  return(result)
}
f2(2)

#Quadratic Functions

f3 <- function(x){
  result <- x^2-4*x+4
  return(result)
}
f3(0)

#Polynomial Functions

f4 <- function(x){
  result <- x^4+x^3+x^2+x+2
  return(result)
}
f4(1)

#Rational Functions

f5 <- function(x){
  result <- 2*x+1/x+3
  return(result)
}
f5(1)